
#include <bits/stdc++.h>

// 1. 함수의 정의
// 2. 베이스 컨디션
// 3. 재귀식

// 1. 정의 n회 만큼 챗봇의 응답을 출력
void Solve(int depth, int n)
{
    std::string s;
    for (int i = 0; i < depth; ++i)
    {
        s += "____";
    }

    // 2.베이스 컨디션
    if (depth == n)
    {
        std::cout << s << "\"재귀함수가 뭔가요?\"\n";
        std::cout << s << "\"재귀함수는 자기 자신을 호출하는 함수라네\"\n";
        std::cout << s << "라고 답변하였지.\n";
        return;
    }

    // 3. 재귀식(유닛단위로 작동할 부분을 찾아야함.)
    std::cout << s << "\"재귀함수가 뭔가요?\"\n";
    std::cout << s << "\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n";
    std::cout << s << "마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n";
    std::cout << s << "그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n";

    Solve(depth + 1, n);
    std::cout << s << "라고 답변하였지.\n";

    return;
}

int main()
{
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(NULL);

    int n;
    std::cin >> n;

    std::cout << "어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n";
    Solve(0, n);

    return 0;
}